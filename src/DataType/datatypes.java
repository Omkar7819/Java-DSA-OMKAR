package DataType;

public class datatypes {
    public static void main(String[] args) {
        byte bytes = 1;
        short shorts = 2;
        int integers = 3;
        long longs = 4L;
        float floats = 5.0f;
        double doubles = 6.0;
        char characters = 'A';
        System.out.println("Byte: " + bytes);
        System.out.println(Byte.SIZE);
        System.out.println("Range From "+Byte.MIN_VALUE+"to"+Byte.MAX_VALUE);
        System.out.println("");

        System.out.println("Short: " + shorts);
        System.out.println(Short.SIZE);
        System.out.println("Range From "+Short.MIN_VALUE+"to"+Short.MAX_VALUE);
        System.out.println("");

        System.out.println("Integer: " + integers);
        System.out.println(Integer.SIZE);
        System.out.println("Range From "+Integer.MIN_VALUE+"to"+Integer.MAX_VALUE);
        System.out.println("");

        System.out.println("Long : " + longs);
        System.out.println(Long.SIZE);
        System.out.println("Range From "+Long.MIN_VALUE+"to"+Long.MAX_VALUE);
        System.out.println("");

        System.out.println("Float : " + floats);
        System.out.println(Float.SIZE);
        System.out.println("Range From "+Float.MIN_VALUE+"to"+Float.MAX_VALUE);
        System.out.println("");

        System.out.println("Double : " + doubles);
        System.out.println(Double.SIZE);
        System.out.println("Range From "+Double.MIN_VALUE+"to"+Double.MAX_VALUE);
        System.out.println("");

        System.out.println("Character : " + characters);
        System.out.println(Character.SIZE);
        System.out.println("Range From "+Character.MIN_VALUE+"to"+Character.MAX_VALUE);
        System.out.println("");

    }
    
}
